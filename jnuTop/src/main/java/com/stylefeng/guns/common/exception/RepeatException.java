package com.stylefeng.guns.common.exception;

public class RepeatException extends Exception{
		private String info;

		public String getInfo() {
			return info;
		}

		public void setInfo(String info) {
			this.info = info;
		}
		public RepeatException(int i){
			this.info = "第" + i +"行有已经插入的数据";
		}
}
